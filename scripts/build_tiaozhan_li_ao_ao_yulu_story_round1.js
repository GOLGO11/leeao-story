function main() {
  const rows = buildRows();
  fs.mkdirSync(OUT_DIR, { recursive: true });
  writeCsv(path.join(OUT_DIR, `${ROUND}.csv`), rows);
  writeTxt(path.join(OUT_DIR, `${ROUND}.txt`), rows);

  const validation = validate(rows);
  const aggregate = writeAggregate();
  const aggregateDuplicatesForThisBook = aggregate.duplicateTextIds.filter((pair) =>
    pair.some((id) => id.startsWith(ID_PREFIX))
  );
  const manifest = {
    book: BOOK,
    slug: SLUG,
    round: ROUND,
    status: "校对轮",
    sourceRoot: path.relative(ROOT, SOURCE_ROOT),
    sourceFiles: sourceFiles(),
    selectionCount: selections.length,
    count: rows.length,
    totalChars: validation.totalChars,
    aggregateCount: aggregate.rows.length,
    aggregateBooks: aggregate.books,
    criteria:
      "短语录合集只保留未在总表出现、能独立复述的外部小故事、笑话、寓言、历史/文学掌故；排除李敖自身短句、政治评语、格言、排比分类、单句比喻和无情节典故。",
    excludedByStandard: [
      "一句话语录、格言、政治讽刺和概念分类不收。",
      "只有典故名或单句影射、没有动作转折和收束的段落不收。",
      "李敖自我夸饰和当代政治人物嘲讽不收。"
    ],
    extractionNotes: [
      "候选扫描覆盖全书 7 个正文文件，自动候选 1 条。",
      "唯一自动候选为排比讽刺句，不是故事。",
      "全书未发现符合当前口径的外部小故事。"
    ],
    proofreadNotes: [
      "校对轮复查 7 个正文文件和候选扫描，维持 0 条入选。",
      "本书主体为每日短句、讽刺语录和格言式分类，没有具备动作转折与收束的外部故事。",
      "唯一自动候选“所有的植物只有洋葱令我哭……”按排比讽刺排除。"
    ],
    aggregateDuplicateTextIds: aggregate.duplicateTextIds,
    generatedAt: new Date().toISOString()
  };

  fs.writeFileSync(
    path.join(OUT_DIR, "story_manifest.json"),
    `${JSON.stringify(manifest, null, 2)}\n`,
    "utf8"
  );
  fs.writeFileSync(
    path.join(OUT_DIR, "story_validation.json"),
    `${JSON.stringify(validation, null, 2)}\n`,
    "utf8"
  );
  writeNotes(rows, validation, aggregate);

  if (!validation.ok) {
    throw new Error(`Validation failed for ${BOOK}: ${JSON.stringify(validation)}`);
  }
  if (aggregateDuplicatesForThisBook.length) {
    throw new Error(
      `Duplicate story text for ${BOOK}: ${JSON.stringify(aggregateDuplicatesForThisBook)}`
    );
  }

  console.log(
    JSON.stringify(
      {
        book: BOOK,
        rows: rows.length,
        aggregateRows: aggregate.rows.length,
        sourceFileCount: manifest.sourceFiles.length,
        ok: validation.ok
      },
      null,
      2
    )
  );
}

main();
